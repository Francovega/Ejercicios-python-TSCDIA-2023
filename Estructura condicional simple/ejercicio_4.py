#Realice un programa que lea dos números y diga cuál es el mayor.

numero_1= int(input("Por favor ingrese un numero: "))
numero_2= int(input("Por favor ingrese un numero: "))

if (numero_1>numero_2):
    print("El primer numero ingresado es mayor")
elif (numero_2>numero_1):
    print ("El segundo numero ingresado es mayor")
else:
    print ("Los numeros ingresados son iguales")
            

